     */
    public function index(Request $request)
    {
        $user = User::where('nickname', 'like', '%' . $request->input('searchKey') . '%')
            ->select('id', 'nickname', 'urlCode', 'avatar')
            ->get();
        return response()->json([
            'user' => $user,
        ], 200);
    }

